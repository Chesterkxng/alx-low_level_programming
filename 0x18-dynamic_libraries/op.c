#include "op.h"
/**
 * add - addition
 * @a: first int
 * @b: second int
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub
 * @a: first int
 * @b: second int
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - mul
 * @a: first int
 * @b: second int
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divide
 * @a: first int
 * @b: second int
 * Return: int
 */
int div(int a, int b)
{
	return ((int) a / b);
}
/**
 * mod - addition
 * @a: first int
 * @b: second int
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
